export const messages: Record<Locale, Dict> = { en, he };

export function translate(locale: Locale, key: string): string {
  return messages[locale]?.[key] ?? messages.en[key] ?? key;
}

/**
 * If a block heading is the English default for its type, return the
 * localized version. Custom user headings pass through unchanged.
 */
const ENGLISH_DEFAULT_HEADINGS: Record<string, string> = {
  header: "",
  summary: "Summary",
  experience: "Work Experience",
  education: "Education",
  skills: "Skills",
  projects: "Projects",
  certifications: "Certifications",
  custom: "Additional",
};

/**
 * Well-known headings the resume seeder writes for non-default
 * experience buckets. Mapping them here lets us localize the section
 * title without changing the underlying DB row — the user can still
 * rename the heading manually and that custom value will pass through
 * unchanged (the lookup miss returns the original string).
 */
const KNOWN_CUSTOM_HEADING_KEYS: Record<string, string> = {
  "Military Service": "canvas.heading.military",
  Volunteering: "canvas.heading.volunteering",
};

export function localizedHeading(
  heading: string,
  type: string,
  t: (k: string) => string,
): string {
  if (heading === ENGLISH_DEFAULT_HEADINGS[type] || heading === "") {
    return t(`canvas.heading.${type}`);
  }
  const known = KNOWN_CUSTOM_HEADING_KEYS[heading];
  if (known) {
    const localized = t(known);
    // Translator falls back to the key itself when the locale lacks
    // the entry; in that case prefer the original English heading.
    return localized === known ? heading : localized;
  }
  return heading;
}

/**
 * Older `addItemToBlock` writes used English seed text ("Degree", "Job
 * Title", etc.) as placeholder values. They should be displayed as empty
 * — the canvas swaps in a localized placeholder, the templates render
 * nothing. Use this everywhere you'd otherwise pipe raw user-editable
 * fields through to the DOM.
 */
const LEGACY_SEEDS = new Set([
  "Degree",
  "University",
  "Job Title",
  "Company",
  "Project Name",
  "New Skill",
  "Other",
  "Certification Name",
]);
export function notLegacy(v: string | null | undefined): string {
  if (!v) return "";
  return LEGACY_SEEDS.has(v) ? "" : v;
}

/**
 * True if every meaningful field of an item is empty (after stripping
 * legacy seed values). Used by template renderers to skip items that
 * would otherwise show as "·" or "in" with nothing on either side.
 */
export function isItemEmpty(
  type: "experience" | "education" | "skill" | "project" | "certification" | "custom",
  data: unknown,
): boolean {
  const d = data as Record<string, string | undefined | null>;
  switch (type) {
    case "experience":
      return !notLegacy(d.title) && !notLegacy(d.company);
    case "education":
      return (
        !notLegacy(d.degree) && !notLegacy(d.institution) && !d.fieldOfStudy
      );
    case "skill":
      return !notLegacy(d.name);
    case "project":
      return !notLegacy(d.name);
    case "certification":
      return !notLegacy(d.name);
    case "custom":
      return !d.title && !d.text;
    default:
      return false;
  }
}
